#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << round(1.2) << endl;
    cout << floor(1.2) << endl;
    cout << ceil(1.2) << endl;
    cout << abs(-1.2) << endl;
    cout << sqrt(1.2) << endl;
    cout << pow(25, 0.5) << endl;
    cout << log(100) << endl;
    cout << log10(1000) << endl;
    cout << log2(32) << endl;
    cout << cbrt(8) << endl;
    return 0;
}
